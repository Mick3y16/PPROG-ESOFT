package centrocomercial;

/**
 * Interface responsável por implementar o método de cálculo do custo de
 * segurança.
 *
 * @author Pedro
 */
public interface Seguranca {

    /**
     * Método abstracto responsável por calcular o custo de segurança.
     *
     * @return Valor do custo de segurança a ser pago por uma loja.
     */
    double calcularSeguranca();

}
